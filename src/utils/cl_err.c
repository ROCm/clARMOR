void check_cl_error(const char * const file_name, const int line_num,
        const cl_int cl_err)
{
    if (cl_err != CL_SUCCESS)
    {
        det_fprintf(stderr, "OpenCL error, exiting application.\n");
        det_fprintf(stderr, "%s:%d: error: %s (%d)\n", file_name, line_num,
                cluErrorString(cl_err), cl_err);
        exit(-1);
    }
    return;
}

void print_program_build_err(cl_context context, cl_program prog,
        cl_int cl_err)
{
    // Print error header
    det_fprintf(stderr, "Error building OpenCL kernel near %s:%d\n",
            __FILE__, __LINE__);

    // Try to get the error log
    size_t num_devs;
    cl_err = clGetContextInfo(context, CL_CONTEXT_DEVICES, 0, NULL,
            &num_devs);
    check_cl_error(__FILE__, __LINE__, cl_err);
    if (num_devs != 0)
    {
        cl_device_id *dev_id = calloc(num_devs, sizeof(cl_device_id));
        cl_err = clGetContextInfo(context, CL_CONTEXT_DEVICES,
                num_devs*sizeof(cl_device_id), dev_id, NULL);
        check_cl_error(__FILE__, __LINE__, cl_err);
        // Determine the size of the log
        size_t log_size;
        cl_err = clGetProgramBuildInfo(prog, dev_id[0],
                CL_PROGRAM_BUILD_LOG, 0, NULL, &log_size);
        check_cl_error(__FILE__, __LINE__, cl_err);

        // Allocate memory for the log
        char *log = (char *) malloc(log_size);
        // Get the log
        cl_err = clGetProgramBuildInfo(prog, dev_id[0],
                CL_PROGRAM_BUILD_LOG, log_size, log, NULL);
        check_cl_error(__FILE__, __LINE__, cl_err);

        // Print the log
        det_fprintf(stderr, "Error log:\n");
        det_fprintf(stderr, "%s\n", log);
        free(log);
        free(dev_id);
    }
    else
        det_fprintf(stderr, "Unable to get build log for device.\n");
}
